// function expression -- in this case we assign the function inside a variable

const happyBirthDay = function (name) {
  return `oh teri Happy BirthDay ${name}`;
};
console.log(happyBirthDay("Singh"));

//  function of sum of three numbers
const sumOfThreeNumbers = function (a, b, c) {
  return a + b + c;
};
console.log(sumOfThreeNumbers(4, 4, 4));
